package variables;

import java.util.EnumMap;

public class UnderstandingVariables {
    public static void main(String[] args) {
        //variable declaration (making a statemen) and assignment
        int age =45;
        System.out.println(age);

        //variable declaration - it reserves / allocates the value.
        String name;
        name ="john";
        System.out.println(name);

        //Variable assignment
        name = "Mike";
        System.out.println(name);

        age= 21;
        System.out.println(age);

    }
}
